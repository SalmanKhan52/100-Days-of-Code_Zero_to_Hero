#  Write a  Python program to demonstrating various dictionary methods.
# Dictionary methods
my_dict = {'apple': 5, 'banana': 3, 'orange': 7}

# Length of dictionary
print(len(my_dict))

# Check if a key exists
print('apple' in my_dict)

# Get value for a key
print(my_dict.get('banana'))

# Remove all items
my_dict.clear()

# Printing the empty dictionary
print(my_dict)
