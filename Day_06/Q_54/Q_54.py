# Write a Python program take two numbers from user and display  the result in  Comparison operators

a=int(input("Enter first  number "))
b=int(input("Enter second number "))

print(a==40) # equal operator (==)
print(a!=b) # not equal operator (!=)

print(a>b) # greater than operator (>)
print(a<b) # less than  operator (<)

print(a>=b)  #  greater than equal to  operator (>=)

print(a<=b) # less than  equal to   operator (<=)



