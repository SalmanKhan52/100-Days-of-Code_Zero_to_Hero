# 75. Write a Python  program to take input week number and print week day.

num=int(input("Enter a Week Day"))
if(num==1):
    print("Monday")
elif(num==2):
    print("Tuesday")
elif(num==3):
    print("Wednesday")
elif(num==4):
    print("Thursday")
elif(num==5):
    print("Friday")
elif(num==6):
    print("Saturday")
else:
    print("Sunday")